// Variables are of 3 types in JavaScript: var, let, and const.
//var let const

alert("hello there! , Welcome to our first JavaScript program ");
// var
var greeting = "Hello, World!";
var a ; // We can declare a variable without initializing it
var a = 10; //We can also declare and initialize a variable
var b = true; // We can declare a variable with a boolean value
var c = 3.14; // We can declare a variable with a float value
var d = null; // We can declare a variable with a null value

// let
let name = "John"; // let allows us to declare a variable that can be reassigned
let age = 25; // let can be used to declare variables with block scope
let isStudent = false; // let can also be used to declare boolean variables
let height = 5.9; // let can be used to declare float variables
let weight = null; // let can also be used to declare variables with null value

// const
const pi = 3.14; // const is used to declare a variable that cannot be reassigned
const country = "USA"; // const can be used to declare a variable with a string value
const isMarried = false; // const can also be used to declare boolean variables
const birthYear = 1998; // const can be used to declare a variable with a number value
